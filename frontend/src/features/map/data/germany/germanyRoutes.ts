import type { BusinessRouteDef } from '../../types/mapTypes';

type R = [from: string, to: string, mode: BusinessRouteDef['mode'], intensity?: number];

function routes(list: R[]): BusinessRouteDef[] {
  return list.map(([from, to, mode, intensity], i) => ({
    id: `de-${mode}-${from}-${to}`,
    fromCityId: from,
    toCityId: to,
    mode,
    active: true,
    delay: (i * 0.05) % 1,
    intensity: intensity ?? 3,
  }));
}

