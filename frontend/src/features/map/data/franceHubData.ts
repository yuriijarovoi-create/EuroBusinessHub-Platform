export function applyFranceHubMetrics(
  cityId: string,
  metrics: MapCityMetrics,
): MapCityMetrics {
  const override = FRANCE_HUB_METRICS[cityId];
  if (!override) return metrics;
  return { ...metrics, ...override };
}

export function isFranceHubCity(cityId: string): boolean {
  return cityId in FRANCE_HUB_METRICS;
}
