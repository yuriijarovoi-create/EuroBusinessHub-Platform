export function getCityById(id: string): City | undefined {
  return cities.find((c) => c.id === id);
}

export function getWorkspaceStats(cityId: string) {
  const city = getCityById(cityId);
  if (!city) return { activeUsers: 0, openOrders: 0, listings: 0 };
  return {
    activeUsers: Math.floor(city.businesses * 0.12),
    openOrders: Math.floor(city.businesses * 0.08),
    listings: Math.floor(city.businesses * 1.4),
  };
}
