/** Normalize city with projected map coordinates */
function withMapXY(c: Omit<City, 'mapX' | 'mapY'> & { mapX?: number; mapY?: number }): City {
  const { mapX, mapY } = latLngToMapXY(c.lat, c.lng);
  return { ...c, mapX, mapY };
}

